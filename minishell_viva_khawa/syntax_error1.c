void	ft_change_q1(int *q)
{
	if ((*q) == 0)
		(*q) = 1;
	else if ((*q) == 1)
		(*q) = 0;
}

void	ft_change_q2(int *q)
{
	if ((*q) == 0)
		(*q) = 2;
	else if ((*q) == 2)
		(*q) = 0;
}
